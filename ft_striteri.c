#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	unsigned int	i;
	char			*t;

	i = 0;
	t = s;
	while (s[i] != 0)
	{
		f(i, t++);
		i++;
	}
}
